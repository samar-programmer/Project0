package com.samar.projects.facebook.entity;

public class LikeDisLikeReplyEntity {
	private int like;
	private int disLike;
	private String reply;
	private String likeFag;
	private String disLikeFlag;
	private String femail;
	private int likeclick;
	private int dislikeclick;
	private int tno;
	
	
	public int getLike() {
		return like;
	}
	public void setLike(int like) {
		this.like = like;
	}
	public int getDisLike() {
		return disLike;
	}
	public void setDisLike(int disLike) {
		this.disLike = disLike;
	}
	public String getReply() {
		return reply;
	}
	public void setReply(String reply) {
		this.reply = reply;
	}
	public String getDisLikeFlag() {
		return disLikeFlag;
	}
	public void setDisLikeFlag(String disLikeFlag) {
		this.disLikeFlag = disLikeFlag;
	}
	public String getLikeFag() {
		return likeFag;
	}
	public void setLikeFag(String likeFag) {
		this.likeFag = likeFag;
	}
	public String getFemail() {
		return femail;
	}
	public void setFemail(String femail) {
		this.femail = femail;
	}
	public int getLikeclick() {
		return likeclick;
	}
	public void setLikeclick(int likeclick) {
		this.likeclick = likeclick;
	}
	public int getDislikeclick() {
		return dislikeclick;
	}
	public void setDislikeclick(int dislikeclick) {
		this.dislikeclick = dislikeclick;
	}
	public int getTno() {
		return tno;
	}
	public void setTno(int tno) {
		this.tno = tno;
	}
}
